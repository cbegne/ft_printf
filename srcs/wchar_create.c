#include "ft_printf.h"

int			wchar_create(wint_t c, char *out, int i)
{
	if (c <= 0x7F)
		out[i++] = c;
	else if (c <= 0x7FF)
	{
		out[i++] = ((c >> 6) | 0xC0);
		out[i++] = ((c & 0x3F) | 0x80);
	}
	else if (c <= 0xFFFF)
	{
		out[i++] = ((c >> 12) | 0xE0);
		out[i++] = (((c >> 6) & 0x3F) | 0x80);
		out[i++] = ((c & 0x3F) | 0x80);
	}
	else if (c <= 0x10FFFF)
	{
		out[i++] = ((c >> 18) | 0xF0);
		out[i++] = (((c >> 12) & 0x3F) | 0x80);
		out[i++] = (((c >> 6) & 0x3F) | 0x80);
		out[i++] = ((c & 0x3F) | 0x80);
	}
	return (i);
}
